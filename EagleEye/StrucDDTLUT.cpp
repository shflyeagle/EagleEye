QMap<quint32, QMap<quint16, DciIdentify> > StrucDDTLUT::dciLUT;
const QMap<quint16, DciIdentify> * StrucDDTLUT::curDciLut = Q_NULLPTR;

void StrucDDTLUT::init()
{
	initStrucLut();
	initDciLut();

	SLUT_SET_FILTER_PREFIX("ddt.");
	SLUT_SET_FILTER_KEY("ddt.blockId");
	SLUT_GEN_CATLUT();
}

void StrucDDTLUT::setCurDciLut()
{
	quint32 dciKey = DDT_DCI_KEY(EeProfile::ddtProfile.tech, 
		EeProfile::ddtProfile.bandwith, 
		EeProfile::ddtProfile.antTx);
	if (!dciLUT.contains(dciKey))
	{
		EELOG_ERROR("StrucDDTLUT::setCurDciLut|no dcikey (" 
			<< dciKey 
			<< ":" 
			<< EeProfile::ddtProfile.tech 
			<< "-" 
			<< EeProfile::ddtProfile.bandwith 
			<< "-" 
			<< EeProfile::ddtProfile.antTx << ")");

		Q_ASSERT(false);
		return;
	}
	
	curDciLut = &(dciLUT[dciKey]);			
}

const QMap<quint16, DciIdentify> * StrucDDTLUT::getCurDciLut()
{
	return curDciLut;
}

quint16 StrucDDTLUT::getDciId(quint16 tbs, quint16 dciFlag)
{
	if (!curDciLut->contains(tbs))
	{
		return DDT_ID_BLOCKINVALID;
	}
	else
	{
		QList<DciIdentify> values = curDciLut->values(tbs);
		foreach(auto v, values)
		{
			if (DDT_DCI_FORMATNA == v.dciFlag  || 
				v.dciFlag == dciFlag)
			{
				return v.dciId;
			}
		}
		return DDT_ID_BLOCKINVALID;
	}
}

