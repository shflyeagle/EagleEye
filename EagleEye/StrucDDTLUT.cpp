bool StrucDDTLUT::isDCIBlock(quint32 sid)
{
	quint16 id = STRUC_N(sid);
	return id >= DDT_DCI_BLOCK_BEGIN && 
		   id <= DDT_DCI_BLOCK_END;
}

QString StrucDDTLUT::getDCIFormat(quint32 sid)
{	
	Q_ASSERT((StrucLUT::strucLut.contains(sid) == true));

	std::shared_ptr<StructProfile> sp = StrucLUT::strucLut[sid];
	return sp->sr.r1Name;
}